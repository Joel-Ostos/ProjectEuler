#include <iostream>
#include <vector>
using namespace std;

// Left and Up movements
int L[] = {1,0}, R[] = {0,1};

int main() 
{
	vector<string> grid = {
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
		"#####################",
	};
	vector<vector<long>> movs(grid.size(), vector<long>(grid.size(), 0));
	movs[0][0] = 1;
	for (int i = 0; i < grid.size(); i++) {
		for (int j = 0; j < grid.size(); j++) {
			for (int k = 0; k < 2; k++) {
				int nx = j-L[k];
				int ny = i-R[k];
				if (nx < 0 || nx >= grid.size() || ny < 0  || ny >= grid.size()) continue;
				movs[i][j] += movs[ny][nx];
			}
		}
	}
	cout << movs[20][20]<< '\n';
}
